select SUM(`TOTAL_FEE`  )  from `SND`.`SND_ORDER` where `GENERATE_TIME`   BETWEEN  '2025-01-01 00:00:00' and '2025-02-01 00:00:00';

-- 系列
SELECT
  SP.`PRODUCT_SERIES`,
  SUM(`SP`.`DISCOUNT_PRICE` * SP.`NUM`),
  COUNT(DISTINCT (SO.`INNER_ORDER_NUM`)),
  COUNT(DISTINCT (SO.`BUYER_NAME`))
FROM
  SND.`SND_PRODUCT` SP
  LEFT JOIN SND.`SND_ORDER` SO ON SP.INNER_ORDER_NUM = SO.INNER_ORDER_NUM
WHERE
  SO.`GENERATE_TIME` BETWEEN '2025-01-01 00:00:00' and '2025-02-01 00:00:00'
GROUP BY
  SP.`PRODUCT_SERIES`;